#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*cur;

	cur = (unsigned char *)s;
	while (n--)
	{
		if (*cur == c)
			return (cur);
		if (n)
			cur++;
	}
	return (NULL);
}
